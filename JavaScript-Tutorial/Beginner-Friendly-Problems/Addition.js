// this program for adding two numbers input by the user
let num1 = parseInt(prompt("Enter the first number : "))
let num2 = parseInt(prompt("Enter the second number : "))
// Arithmatic Operators
// 1. Addition : num1 + num2
let sum = num1 + num2;
console.log("Addition of two given numbers is : "+sum);

// 2. Subtraction : num1 - num2
let difference = num1 - num2;
console.log("Subtraction of two given numbers is : "+difference);


